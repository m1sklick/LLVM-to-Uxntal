#ifndef UxnSUBTARGET_H
#define UxnSUBTARGET_H

#include "Uxn.h"
#include "UxnFrameLowering.h"
#include "UxnISelLowering.h"
#include "UxnInstrInfo.h"
#include "UxnSelectionDAGInfo.h"
#include "UxnSubtarget.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "UxnGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class UxnSubtarget : public UxnGenSubtargetInfo {
  virtual void anchor();

private:
  const DataLayout DL;       // Calculates type size & alignment.
  UxnInstrInfo InstrInfo;
  UxnTargetLowering TLInfo;
  UxnSelectionDAGInfo TSInfo;
  UxnFrameLowering FrameLowering;
  InstrItineraryData InstrItins;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  UxnSubtarget(const Triple &TT, StringRef CPU,
               StringRef FS, UxnTargetMachine &TM);

  /// getInstrItins - Return the instruction itineraries based on subtarget
  /// selection.
  const InstrItineraryData *getInstrItineraryData() const override {
    return &InstrItins;
  }
  const UxnInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const UxnRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const UxnTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const UxnFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const UxnSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);
};
} // End llvm namespace

#endif

